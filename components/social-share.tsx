"use client"

import { useState } from "react"
import { Button } from "@/components/ui/button"
import { Card } from "@/components/ui/card"
import { 
  Facebook, 
  Twitter, 
  Share2, 
  Copy, 
  Instagram,
  Check
} from "lucide-react"
import { motion } from "framer-motion"

interface SocialShareProps {
  url: string
  title: string
  description?: string
  image?: string
  hashtags?: string[]
}

export function SocialShare({ 
  url, 
  title, 
  description = "", 
  image = "",
  hashtags = []
}: SocialShareProps) {
  const [copied, setCopied] = useState(false)
  const [showShare, setShowShare] = useState(false)

  const fullUrl = url.startsWith('http') ? url : `https://triskideas.com${url}`
  const encodedUrl = encodeURIComponent(fullUrl)
  const encodedTitle = encodeURIComponent(title)
  const encodedDescription = encodeURIComponent(description)
  const encodedImage = encodeURIComponent(image.startsWith('http') ? image : `https://triskideas.com${image}`)
  const encodedHashtags = hashtags.map(tag => tag.replace(/^#/, '')).join(',')

  const shareLinks = {
    facebook: `https://www.facebook.com/sharer/sharer.php?u=${encodedUrl}`,
    twitter: `https://twitter.com/intent/tweet?text=${encodedTitle}&url=${encodedUrl}&hashtags=${encodedHashtags}`,
    whatsapp: `https://wa.me/?text=${encodedTitle}%20${encodedUrl}`,
    instagram: `https://www.instagram.com/`, // Instagram doesn't support direct sharing via URL
  }

  const copyToClipboard = async () => {
    try {
      await navigator.clipboard.writeText(fullUrl)
      setCopied(true)
      setTimeout(() => setCopied(false), 2000)
    } catch (err) {
      console.error('Failed to copy URL:', err)
    }
  }

  const handleNativeShare = async () => {
    if (navigator.share) {
      try {
        await navigator.share({
          title,
          text: description,
          url: fullUrl,
        })
      } catch (err) {
        console.error('Error sharing:', err)
      }
    } else {
      setShowShare(!showShare)
    }
  }

  const socialButtons = [
    {
      name: 'WhatsApp',
      icon: () => (
        <svg viewBox="0 0 24 24" className="w-5 h-5 fill-current">
          <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.890-5.335 11.893-11.893A11.821 11.821 0 0020.893 3.488"/>
        </svg>
      ),
      color: 'bg-green-500 hover:bg-green-600',
      onClick: () => window.open(shareLinks.whatsapp, '_blank'),
    },
    {
      name: 'Facebook',
      icon: Facebook,
      color: 'bg-blue-600 hover:bg-blue-700',
      onClick: () => window.open(shareLinks.facebook, '_blank'),
    },
    {
      name: 'X (Twitter)',
      icon: Twitter,
      color: 'bg-gray-900 hover:bg-black',
      onClick: () => window.open(shareLinks.twitter, '_blank'),
    },
    {
      name: 'Instagram',
      icon: Instagram,
      color: 'bg-gradient-to-r from-purple-500 to-pink-500 hover:from-purple-600 hover:to-pink-600',
      onClick: () => {
        // For Instagram, we'll copy the text and URL, then open Instagram
        copyToClipboard()
        window.open(shareLinks.instagram, '_blank')
        alert('Link copied! You can now paste it in your Instagram story or post.')
      },
    },
  ]

  return (
    <div className="space-y-4">
      {/* Main Share Button */}
      <Button
        onClick={handleNativeShare}
        className="bg-amber-700 hover:bg-amber-800 text-white px-6 py-3 rounded-lg flex items-center gap-2 w-full sm:w-auto"
      >
        <Share2 className="w-4 h-4" />
        Share this Article
      </Button>

      {/* Social Share Options - Always visible */}
      <motion.div
        initial={{ opacity: 0, y: 10 }}
        animate={{ opacity: 1, y: 0 }}
        exit={{ opacity: 0, y: 10 }}
        className="space-y-4"
      >
        <Card className="p-4 bg-white border border-amber-200">
          <h3 className="text-lg font-semibold text-amber-900 mb-3">Share on Social Media</h3>

          {/* Social Platform Buttons */}
          <div className="grid grid-cols-2 sm:grid-cols-4 gap-3 mb-4">
            {socialButtons.map((platform) => (
              <Button
                key={platform.name}
                onClick={platform.onClick}
                className={`${platform.color} text-white flex flex-col items-center gap-2 py-3 h-auto transition-all duration-200 hover:scale-105`}
              >
                <platform.icon className="w-5 h-5" />
                <span className="text-xs font-medium">{platform.name}</span>
              </Button>
            ))}
          </div>

          {/* Copy URL Section */}
          <div className="border-t border-amber-100 pt-3">
            <p className="text-sm text-gray-600 mb-2">Or copy the link:</p>
            <div className="flex gap-2">
              <input
                type="text"
                value={fullUrl}
                readOnly
                className="flex-1 px-3 py-2 border border-gray-300 rounded-md text-sm bg-gray-50 text-gray-700"
              />
              <Button
                onClick={copyToClipboard}
                variant="outline"
                className="px-3 py-2 border-amber-300 hover:bg-amber-50"
              >
                {copied ? (
                  <Check className="w-4 h-4 text-green-600" />
                ) : (
                  <Copy className="w-4 h-4" />
                )}
              </Button>
            </div>
            {copied && (
              <motion.p
                initial={{ opacity: 0 }}
                animate={{ opacity: 1 }}
                className="text-sm text-green-600 mt-1"
              >
                Link copied to clipboard!
              </motion.p>
            )}
          </div>
        </Card>
      </motion.div>
    </div>
  )
}